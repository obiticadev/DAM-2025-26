package Clases;

public class Calendario {
    private int numDia;
    private String sorpresa;
    private boolean estado;

    public Calendario() {
    }

    public int getNumDia() {
        return numDia;
    }

    public String getSorpresa() {
        return sorpresa;
    }

    public boolean getEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }

}
